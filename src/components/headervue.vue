<script setup>
import { defineEmits, ref } from 'vue'

const emit = defineEmits(['change-page', 'toggle-list-icon']);
function change_page(page) {

  let all_nav_links = document.getElementsByClassName("nav-link");
  for (let i = 0; i < all_nav_links.length; i++) {
    if (all_nav_links[i].classList.contains("active")) {
      all_nav_links[i].classList.remove("active");
    }
  }
  document.getElementById(page + "-link").classList.add("active");

  emit('change-page', page);
}
function toggle_list_icon() {
  emit('toggle-list-icon');
}
</script>